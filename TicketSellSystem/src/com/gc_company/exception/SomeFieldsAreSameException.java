package com.gc_company.exception;

public class SomeFieldsAreSameException extends Exception {

	public SomeFieldsAreSameException() {
		super();
	}

	public SomeFieldsAreSameException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public SomeFieldsAreSameException(String message, Throwable cause) {
		super(message, cause);
	}

	public SomeFieldsAreSameException(String message) {
		super(message);
	}

	public SomeFieldsAreSameException(Throwable cause) {
		super(cause);
	}

}
