package com.ssm.exception.gametypeexception;

public class GameTypeExistedException extends Exception {

	public GameTypeExistedException() {
		super();
	}

	public GameTypeExistedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public GameTypeExistedException(String arg0) {
		super(arg0);
	}

	public GameTypeExistedException(Throwable arg0) {
		super(arg0);
	}

}
