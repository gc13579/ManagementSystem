package com.ssm.exception.gametypeexception;

public class GameTypeContainsGamesException extends Exception {

	public GameTypeContainsGamesException() {
		super();
	}

	public GameTypeContainsGamesException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public GameTypeContainsGamesException(String arg0) {
		super(arg0);
	}

	public GameTypeContainsGamesException(Throwable arg0) {
		super(arg0);
	}

}
