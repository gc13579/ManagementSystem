package com.ssm.exception.fileuploadexception;

public class UploadTypeException extends Exception {

	public UploadTypeException() {
		super();
	}

	public UploadTypeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UploadTypeException(String arg0) {
		super(arg0);
	}

	public UploadTypeException(Throwable arg0) {
		super(arg0);
	}

}
