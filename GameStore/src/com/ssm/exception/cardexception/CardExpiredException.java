package com.ssm.exception.cardexception;

public class CardExpiredException extends Exception {

	public CardExpiredException() {
		super();
	}

	public CardExpiredException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CardExpiredException(String arg0) {
		super(arg0);
	}

	public CardExpiredException(Throwable arg0) {
		super(arg0);
	}

}
