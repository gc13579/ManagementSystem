package com.ssm.exception.cardexception;

public class CardPwdMismatchException extends Exception {

	public CardPwdMismatchException() {
		super();
	}

	public CardPwdMismatchException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CardPwdMismatchException(String arg0) {
		super(arg0);
	}

	public CardPwdMismatchException(Throwable arg0) {
		super(arg0);
	}

}
