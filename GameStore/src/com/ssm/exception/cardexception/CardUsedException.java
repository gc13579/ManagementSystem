package com.ssm.exception.cardexception;

public class CardUsedException extends Exception {

	public CardUsedException() {
		super();
	}

	public CardUsedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CardUsedException(String arg0) {
		super(arg0);
	}

	public CardUsedException(Throwable arg0) {
		super(arg0);
	}

}
