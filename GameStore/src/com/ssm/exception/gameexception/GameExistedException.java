package com.ssm.exception.gameexception;

public class GameExistedException extends Exception {

	public GameExistedException() {
		super();
	}

	public GameExistedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public GameExistedException(String arg0) {
		super(arg0);
	}

	public GameExistedException(Throwable arg0) {
		super(arg0);
	}

}
